 <b>Databib</b>
 <p>&nbsp;</p>

 <p>Databib is a collaboration between the Purdue University Libraries and Penn State University to create a community-driven, annotated bibliography of research data repositories.  It will also serve as a testbed for linking, integrating, and presenting information about research data providers in Web 1.0, 2.0, and 3.0 contexts.  This work is supported in part by a <a href="http://www.imls.gov/applicants/detail.aspx?GrantId=19">Sparks! Innovation National Leadership Grant</a> from the Institute of Museum and Library Services. Databib is a nine-month project that will conclude development in May 2012.</p> 
<p>As the project progresses, updates will be posted on this website. For more information, <a href="mailto:databib@gmail.com">email us</a>.</p>


<p><i>July 18, 2012</i></p>
<p>Databib, <a href="http://databib.org">http://databib.org</a>, is a tool for helping people to identify and locate online repositories of research data.  Over 200 data repositories have been cataloged in Databib, with more being added every week. Users and bibliographers create and curate records that describe data repositories that users can browse and search.</p>

<p><ul>
<li style="list-style-image:url(/images/bullet.gif);">What repositories are appropriate for a researcher to submit his or her data to?</li>
<li style="list-style-image:url(/images/bullet.gif);">How do users find appropriate data repositories and discover datasets to meet their needs?</li>
<li style="list-style-image:url(/images/bullet.gif);">How can librarians help patrons locate and integrate data into their research or learning?</li>
</ul></p>

<p>Databib begins to address these needs for data users, data producers, publishers, librarians, funding agencies, and others engaged in data-driven research.</p>

<p>In addition to the website, Databib is made available using a variety of machine interfaces (RSS, RDF/XML, OpenSearch, RDFa/Linked Data) for easy integration with other tools and environments without restriction (CC0). Databib's international advisory board represents support for collaborating to develop such a global registry of research data repositories.</p>

<p>The development of Databib was initially supported by a Sparks! Innovation National Leadership Grant from the Institute of Museum and Library Services.</p>

<h5>CALL FOR EDITORS</h5>

<p>Nominations for an Editorial Board are being solicited to ensure the coverage and accuracy of Databib. Editors ideally will have expertise in a specific research domain or knowledge of research data repositories in a particular geographic region as well as experience with descriptive metadata.  The primary role of an Editor is to review, edit, and approve submissions to Databib and contribute to the enhancement of the metadata and functionality of Databib for a voluntary, three-year term. The Editorial Board will meet (virtually) a minimum of twice a year and will correspond as needed by email.</p>

<p>Please send nominations or questions to <a href="mailto:databib@gmail.com">databib@gmail.com</a>. Thank you.</p>
